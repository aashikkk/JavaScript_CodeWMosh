// Performing Calculations like we have in mathematics

let x = 10
let y = 3

console.log(x + y);
// 2 Operands are x and y
// Operator is +
// x+y refer to an expression. Expressions is something
// that produce a value

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);  // division
console.log(x % y);  // remainder of division
console.log(x ** y);    //exponential

// Increment (++)
console.log(x++); //10
console.log(x); //11

// console.log(++x); //11

// Decrement (--)
console.log(--x); //9
// Operator got worked and printed